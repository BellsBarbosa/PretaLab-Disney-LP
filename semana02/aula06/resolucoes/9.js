/*
 Defina 2 constantes que salvem valores numericos diferentes. Utilize if/else 
 para escrever um código que retorne o maior dos dois números
*/

const numero1 = 12;
const numero2 = 30;

if (numero2 > numero1) {
  console.log(numero2 + " é o maior!");
} else {
  console.log(numero1 + " é o maior!");
}
